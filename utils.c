#include "pusw.h"

int	atoi_mod(char *str)
{
	int	i;

	i = -1;
	if (!str)
		return (-1);
	while (str[i] >= '0' && str[i] <= '9')
	{
		i = i * 10 + (str[i] - '0');
		i++;
	}
	return (i);
}